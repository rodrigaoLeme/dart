String _localize(String key) {
  var l = Language.instance.current;
  Map<String, String> map;
  switch (l) {
    case "es":
      map = _esWords;
    case "en":
      map = _enWords;
    case "pt":
      map = _ptWords;
    case "fr":
      map = _frWords;
    case "zh-CN":
      map = _zhCNWords;
    default:
      map = _enWords;
  }

  if (!map.containsKey(key)) {
    debugPrint("LOCALIZATION MISSING: '$key' not available for '$l'");
  }

  return map[key] ?? key;
}

